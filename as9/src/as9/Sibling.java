package as9;

public class Sibling 
{
	private String name;
	private int age;
	private int weight;
	
	public Sibling (String n, int a, int w)
	{
		
		name=n;
		age=a;
		weight=w;
	}
	
	public String getName()
	{
		return name;
	
	}
	
	public int getAge()
	{
		return age;
	}
	
	public int getWeight()
	{
		return weight;
	}

}
